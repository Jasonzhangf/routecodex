function collectTsFiles(dir, out = []) {
  for (const entry of fs.readdirSync(dir)) {
    const fullPath = path.join(dir, entry);
    const stat = fs.statSync(fullPath);
    if (stat.isDirectory()) {
      if (!['dist', 'node_modules', 'build', 'coverage'].includes(entry)) {
        collectTsFiles(fullPath, out);
      }
      continue;
    }
    if (fullPath.endsWith('.ts') || fullPath.endsWith('.tsx')) {
      out.push(fullPath);
    }
  }
  return out;
}

function collectServertoolWrapperFunctionRefs() {
  const refs = new Set();
  const importOrExportPattern =
    /^(?:import|export)\s+(?:type\s+)?\{([^;]*?)\}\s+from\s+['"]rcc-llmswitch-core\/native\/servertool-wrapper['"];$/gm;
  for (const file of collectTsFiles(sharedmoduleSrcDir)) {
    const content = fs.readFileSync(file, 'utf8');
    for (const match of content.matchAll(importOrExportPattern)) {
      const bindings = match[1].split(',');
      for (const binding of bindings) {
        const rawName = binding
          .trim()
          .replace(/^type\s+/, '')
          .split(/\s+as\s+/)[0]
          ?.trim();
        if (rawName && rawName.endsWith('WithNative')) {
          refs.add(rawName);
        }
      }
    }
  }
  return refs;
}

function pruneServertoolWrapperDeclarations(source, activeRefs) {
  const kept = new Set();
  const pruned = source.replace(
    /^export declare function ([A-Za-z0-9_]+)(?:<[^>]+>)?\([\s\S]*?\): [^;]+;$/gm,
    (line, name) => {
      if (!activeRefs.has(name)) {
        return '';
      }
      kept.add(name);
      return line;
    }
  );
  const missing = [...activeRefs].filter((name) => !kept.has(name)).sort();
  if (missing.length > 0) {
    throw new Error(`servertool-wrapper.d.ts missing active declarations: ${missing.join(', ')}`);
  }
  if (/\bany\b/.test(pruned)) {
    throw new Error('servertool-wrapper.d.ts contains forbidden `any` token');
  }
  return pruned.replace(/\n{3,}/g, '\n\n').trimEnd() + '\n';
}

