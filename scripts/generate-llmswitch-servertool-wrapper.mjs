#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const outDir = path.join(root, 'sharedmodule', 'llmswitch-core', 'dist', 'native');
const jsPath = path.join(outDir, 'servertool-wrapper.js');
const dtsPath = path.join(outDir, 'servertool-wrapper.d.ts');
const sharedmoduleSrcDir = path.join(root, 'sharedmodule', 'llmswitch-core', 'src');

const js = `// Generated by scripts/generate-llmswitch-servertool-wrapper.mjs
// Servertool runtime semantics are Rust-owned. This package subpath is retained
// only as an importable release marker; production per-capability TS wrappers
// are intentionally not exported.
export {};
`;

const dts = `// Generated by scripts/generate-llmswitch-servertool-wrapper.mjs
// Servertool runtime semantics are Rust-owned. This package subpath intentionally
// exposes no per-capability native wrapper declarations.
export {};
`;

function collectTsFiles(dir, out = []) {
  if (!fs.existsSync(dir)) {
    return out;
  }
  for (const entry of fs.readdirSync(dir)) {
    const fullPath = path.join(dir, entry);
    const stat = fs.statSync(fullPath);
    if (stat.isDirectory()) {
      if (!['dist', 'node_modules', 'build', 'coverage'].includes(entry)) {
        collectTsFiles(fullPath, out);
      }
      continue;
    }
    if (fullPath.endsWith('.ts') || fullPath.endsWith('.tsx')) {
      out.push(fullPath);
    }
  }
  return out;
}

function collectServertoolWrapperFunctionRefs() {
  const refs = [];
  const importOrExportPattern =
    /^(?:import|export)\s+(?:type\s+)?\{([^;]*?)\}\s+from\s+['"]rcc-llmswitch-core\/native\/servertool-wrapper['"];$/gm;
  for (const file of collectTsFiles(sharedmoduleSrcDir)) {
    const content = fs.readFileSync(file, 'utf8');
    for (const match of content.matchAll(importOrExportPattern)) {
      const bindings = match[1].split(',');
      for (const binding of bindings) {
        const rawName = binding
          .trim()
          .replace(/^type\s+/, '')
          .split(/\s+as\s+/)[0]
          ?.trim();
        if (rawName && rawName.endsWith('WithNative')) {
          refs.push(`${path.relative(root, file)}:${rawName}`);
        }
      }
    }
  }
  return refs.sort();
}

const activeRefs = collectServertoolWrapperFunctionRefs();
if (activeRefs.length > 0) {
  throw new Error(
    `servertool-wrapper package subpath must not expose per-capability WithNative declarations; active refs: ${activeRefs.join(', ')}`
  );
}

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(jsPath, js);
fs.writeFileSync(dtsPath, dts);
console.log(`[servertool-wrapper] generated ${path.relative(root, jsPath)} and ${path.relative(root, dtsPath)}`);
