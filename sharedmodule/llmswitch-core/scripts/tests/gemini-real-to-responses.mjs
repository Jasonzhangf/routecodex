#!/usr/bin/env node
/**
 * Real Gemini test → align to OpenAI Responses
 * - Calls Google Gemini generateContent (non-stream) with provided API key
 * - Extracts text, builds Responses JSON
 * - Produces Responses SSE and verifies roundtrip
 */

import assert from 'node:assert/strict';
import { Readable } from 'node:stream';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const { responsesConverters } = await import('../../dist/sse/index.js');

const API_KEY = process.env.GEMINI_API_KEY;
if (!API_KEY) {
  console.error('Missing GEMINI_API_KEY env. Example: GEMINI_API_KEY=... npm run test:gemini:real');
  process.exit(1);
}

const MODELS = [
  'gemini-2.5-flash-lite',
  'gemini-2.0-flash-lite',
  'gemini-1.5-flash'
];

async function callGemini(model, prompt) {
  const url = `https://generativelanguage.googleapis.com/v1beta/models/${encodeURIComponent(model)}:generateContent?key=${encodeURIComponent(API_KEY)}`;
  const resp = await fetch(url, {
    method: 'POST',
    headers: { 'content-type': 'application/json' },
    body: JSON.stringify({ contents: [ { role: 'user', parts: [ { text: prompt } ] } ] })
  });
  const text = await resp.text();
  let json = null;
  try { json = JSON.parse(text); } catch {}
  if (!resp.ok) {
    throw new Error(`HTTP ${resp.status}: ${text}`);
  }
  return json;
}

function extractText(json) {
  const cand = (json?.candidates || [])[0];
  const parts = cand?.content?.parts || [];
  for (const p of parts) {
    if (p && typeof p === 'object' && typeof p.text === 'string') return p.text;
  }
  return '';
}

function buildResponsesFromText(model, text) {
  const now = Math.floor(Date.now()/1000);
  return {
    id: `resp_${now}`,
    object: 'response',
    created_at: now,
    status: 'completed',
    model,
    output: [ { id: `msg_${now}`, type: 'message', status: 'completed', role: 'assistant', content: [ { type: 'input_text', text } ] } ]
  };
}

async function main() {
  const prompt = 'Please reply a short sentence saying hello from Gemini real test.';
  let json = null;
  let modelUsed = null;
  let lastErr = null;
  for (const m of MODELS) {
    try {
      json = await callGemini(m, prompt);
      modelUsed = m;
      break;
    } catch (e) {
      lastErr = e;
    }
  }
  if (!json) throw lastErr || new Error('Gemini call failed');
  const text = extractText(json);
  if (!text) throw new Error('No text generated by Gemini');
  const resp = buildResponsesFromText(modelUsed, text);

  // SSE outputs and roundtrip
  const sse = await responsesConverters.jsonToSse.convertResponseToJsonToSse(resp, { requestId: 'gemini-real', model: modelUsed });
  const round = await responsesConverters.sseToJson.convertSseToJson(sse, { requestId: 'gemini-real-2', model: modelUsed });
  const t2 = round.output?.[0]?.content?.[0]?.text || '';
  assert.strictEqual(t2, text, 'Responses SSE roundtrip mismatch');
  console.log('✅ Gemini real → Responses SSE roundtrip passed');
  console.log(`Model: ${modelUsed}`);
  console.log(`Text: ${text}`);
}

main().catch((e) => { console.error('❌ Gemini real test failed:', e); process.exit(1); });

